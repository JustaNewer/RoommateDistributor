<template>
  <Transition name="toast">
    <div v-if="show" class="toast" :class="type">
      {{ message }}
    </div>
  </Transition>
</template>

<script>
export default {
  name: 'SystemToast',
  props: {
    show: {
      type: Boolean,
      required: true
    },
    message: {
      type: String,
      required: true
    },
    type: {
      type: String,
      default: 'success',
      validator: (value) => ['success', 'error', 'info'].includes(value)
    }
  }
}
</script>

<style scoped>
